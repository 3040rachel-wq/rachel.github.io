function openModal(name) {
  document.getElementById('organName').innerText = name;
  document.getElementById('organDesc').innerText = organData[name];
  document.getElementById('organModal').style.display = 'flex';
}

function closeModal() {
  document.getElementById('organModal').style.display = 'none';
}

// סגירה בלחיצה מחוץ לחלון
window.onclick = function(event) {
  const modal = document.getElementById('organModal');
  if (event.target == modal) {
    modal.style.display = 'none';
  }
}
